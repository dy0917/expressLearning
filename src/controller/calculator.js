const add = (num1, num2) => {
  return parseFloat(num1) + parseFloat(num2);
};

const minus = (num1, num2) => {
  return num1 - num2;
};

const divide = (num1, num2) => {
  return num1 / num2;
};
const multiple = (num1, num2) => {
  return num1 / num2;
};

module.exports = { add, minus, divide, multiple };
